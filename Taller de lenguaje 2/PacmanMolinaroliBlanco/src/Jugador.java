/**
 * Esta clase contiene al Jugador y todos los metodos y variables del mismo
 * @author Valentin Blanco y Carlos Molinaroli
 *
 */
public class Jugador {
	/**
	 * variable int para indicar el puntaje del Jugador
	 */
	private int puntaje;
	/**
	 * @return retorna el puntaje que tiene el Jugador
	 */
	public int getPuntaje() {
		return puntaje;
	}
	/**
	 * @param puntaje, puntaje que se le asignara al puntaje actual
	 */
	public void setPuntaje(int puntaje) {
		this.puntaje = puntaje;
	}
	
}
